
class Calculator():

    def add(self, a, b):
        print(f"Lets do addition: {a + b}")
        return a + b

    def multiply(self, a, b):
        print(f"Lets do multiplication: {a * b}")
        return a * b

    def subtract(self, a, b):
        print(f"Lets do subtraction: {a - b}")
        return a - b


    def divide(self, a, b):
        print(f"Lets do division: {a / b}")
        return a / b